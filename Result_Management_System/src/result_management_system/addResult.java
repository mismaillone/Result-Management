    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String roll = rollno.getText();
        String physics = phy.getText();
        String maths = Math.getText();
        String netw = Net.getText();
        String dbms = DBMS.getText();
        String os = OS.getText();
        
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/college", "root", "ismaillone");
            Statement st = con.createStatement();
          ResultSet rs = st.executeQuery("select * from student where rollno = '"+roll+"'");
          if (rs.next()) {
           st.executeUpdate("INSERT INTO result (rollno, physics, maths, networking, dbms, os) VALUES ('" + roll + "', '" + physics + "','" + maths + "','" + netw + "','" + dbms + "','" + os + "')");
          JOptionPane.showMessageDialog(null,"Marks Added sucessfully");
          setVisible(false);
          new addResult().setVisible(true);
          }else{
              JOptionPane.showMessageDialog(null,"This Roll No. is not registered");
          }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed


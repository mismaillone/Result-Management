    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String course =(String)Course.getSelectedItem();
        String branch = (String)bname.getSelectedItem();
        String rollno = (String)rollNo.getText();
        String name = (String)Name.getText();
        String gender = (String)Gender.getSelectedItem();
        String fatherName = (String)fname.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/college", "root", "ismaillone");
            
            Statement st = con.createStatement();
           st.executeUpdate("INSERT INTO student(rollno, course, branch, name, gender, fatherName) VALUES ('" + rollno + "', '" + course + "', '" + branch + "', '" + name + "', '" + gender + "', '" + fatherName + "')");
            JOptionPane.showMessageDialog(null, "Data saved sucessfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
            
    }//GEN-LAST:event_saveActionPerformed

